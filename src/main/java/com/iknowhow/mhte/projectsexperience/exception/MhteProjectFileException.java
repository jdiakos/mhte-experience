package com.iknowhow.mhte.projectsexperience.exception;

public class MhteProjectFileException extends RuntimeException {

    public MhteProjectFileException() {
        super();
    }

    public MhteProjectFileException(String message) {
        super(message);
    }

    public MhteProjectFileException(String message, Throwable cause) {
        super(message, cause);
    }

    public MhteProjectFileException(Throwable cause) {
        super(cause);
    }
}
